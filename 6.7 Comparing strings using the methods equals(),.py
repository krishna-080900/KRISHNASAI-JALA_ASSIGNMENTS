#6.7 Comparing strings using the methods equals(),

string1 = "Hello"
string2 = "Hello"
string3 = "World"

# Using '==' to compare strings
print(string1 == string2)  # True, because the strings are equal
print(string1 == string3)  # False, because the strings are different
str8 = 'Itachi uchiha'
str1 = 'Madara uchiha'
str2 = str8
print(str8 == str1)
print(str8 == str2)
print(str1 == str2)
print(str8 != str1)
