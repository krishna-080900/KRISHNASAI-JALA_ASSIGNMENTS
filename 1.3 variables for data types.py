# 1.3 Define variables for different Data Types int, Boolean, char, float, double and print on the Console.


int = 10              # Integer type
boolean = True        # Boolean type
char = 'A'            # Character type (single character string in Python)
float = 3.14          # Float type
double = 3.14159      # Double type (Python uses 'float' for both float and double)

# Print the values to the console
print("Integer:", int)
print("Boolean:", boolean)
print("Character:", char)
print("Float:", float)
print("Double:", double)
